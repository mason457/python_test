import numpy as np
print("-------------------------------------------------")
#資料
arr = np.array([[2,4,6,8],[1,3,5,7],[10,12,14,16],[9,11,13,15]])
#根據第一維度平均切割2個
result1 = np.vsplit(arr,2)
print(result1)
#[2,4,6,8],[1,3,5,7]
#[10,12,14,16],[9,11,13,15]
print("=================================================")
#根據第二維度平均切割2個
result2 = np.hsplit(arr,2)
print(result2)
#[2,4],[1,3],[10,12],[9,11]
#[6,8],[5,7],[14,16],[13,15]
print("=================================================")
#根據第二維度平均切割4個
result3 = np.hsplit(arr,4)
print(result3)
#[2],[1],[10,],[9]
#[4],[3],[12],[11]
#[6],[5],[14],[13]
#[8],[7],[16],[15]